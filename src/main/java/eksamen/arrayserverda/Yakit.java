package eksamen.arrayserverda;

public class Yakit {
    private String yakit1;
    private String yakit2;

    public Yakit(String yakit1, String yakit2) {
        this.yakit1 = yakit1;
        this.yakit2 = yakit2;
    }

    public Yakit(){}

    public String getYakit1() {
        return yakit1;
    }

    public void setYakit1(String yakit1) {
        this.yakit1 = yakit1;
    }

    public String getYakit2() {
        return yakit2;
    }

    public void setYakit2(String yakit2) {
        this.yakit2 = yakit2;
    }
}
